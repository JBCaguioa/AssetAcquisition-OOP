    private void reportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportButtonActionPerformed
        jLabel7.setText(jComboBox1.getSelectedItem().toString());
        setOffice();
        clearDisplayTable();
        int choice;
        if (jRadioButton6.isSelected()) {
            choice = 1;
            items = record.getRecord(choice).ShowRecord(office);
        } else if (jRadioButton5.isSelected()) {
            choice = 2;
            items = record.getRecord(choice).ShowRecord(office);
        } else if (jRadioButton7.isSelected()) {
            choice = 3;
            items = record.getRecord(choice).ShowRecord(office);
        }

        model = (DefaultTableModel) jTable1.getModel();
        for (Item i : items) {
            model.addRow(new Object[]{i.getItemNum(), i.getItemName(), i.getItemCondition()});
        }
        
    }//GEN-LAST:event_reportButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        getInfo();
        Item item = new Item(itemNum, itemName, itemCondition);
        if (items.size() >= 5) {
            infoBox("Maximum of 5 items only per office", "Add failed" );
        }
        else
        strat.crud(new Add(office, item));
        refresh();
        clearSelection();
    }//GEN-LAST:event_addButtonActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // get the model from the jtable
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // get the selected row index
        int selectedRowIndex = jTable1.getSelectedRow();

        // set the selected row data into jtextfields
        jTextField2.setText(model.getValueAt(selectedRowIndex, 0).toString());
        jTextField1.setText(model.getValueAt(selectedRowIndex, 1).toString());
        String value = model.getValueAt(selectedRowIndex, 2).toString();
        if (value.equals("Good condition")) {
            jRadioButton1.setSelected(true);
            condition = "Good condition";
        } else if (value.equals("Poor condition")) {
            jRadioButton2.setSelected(true);
            condition = "Poor condition";
        } else if (value.equals("Missing")) {
            jRadioButton3.setSelected(true);
            condition = "Missing";
        } else if (value.equals("Needs to be disposed")) {
            jRadioButton4.setSelected(true);
            condition = "Needs to be disposed";
        }

        oldItemNum = Integer.parseInt(jTextField2.getText());
    }//GEN-LAST:event_jTable1MouseClicked

