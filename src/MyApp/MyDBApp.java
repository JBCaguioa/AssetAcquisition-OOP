    public void clearDisplayTable() {
        model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged(); // notifies the JTable that the model has changed
    }

    public void refresh() {
        setOffice();
        clearDisplayTable();
        items = record.getRecord(3).ShowRecord(office);

        showRadioGroup.clearSelection();
        jRadioButton7.setSelected(true);
        model = (DefaultTableModel) jTable1.getModel();
        for (Item i : items) {
            model.addRow(new Object[]{i.getItemNum(), i.getItemName(), i.getItemCondition()});
        }
    }

    public void setOffice() {
        String tempOffice = jComboBox1.getSelectedItem().toString();

        if (tempOffice.equals("IT Office")) {
            office = "IT_OFFICE";
        } else if (tempOffice.equals("Accounting Office")) {
            office = "ACCOUNTING_OFFICE";
        }
    }

    public void getInfo() {
        setOffice();
        itemNum = Integer.parseInt(jTextField2.getText());
        itemName = jTextField1.getText();
        itemCondition = condition;
    }

    public void clearSelection() {
        jTextField1.setText("");
        jTextField2.setText("");
        conditionRadioGroup.clearSelection();
    }
